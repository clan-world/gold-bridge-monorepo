#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { readEnvFile, repoRootFromScript, loadDeployment, chainConfig, transceiverAddress, fail } from './lib/deployment.mjs';

const root = repoRootFromScript();
const env = readEnvFile(path.join(root, '.env'));
const deployment = loadDeployment(root, env);

const solanaChainName = env.NTT_SOLANA_CHAIN || 'Solana';
const baseChainName = env.NTT_BASE_CHAIN || 'BaseSepolia';
const solana = chainConfig(deployment, solanaChainName);
const base = chainConfig(deployment, baseChainName);

if (!solana) fail(`Missing ${solanaChainName} in deployment.json`);
if (!base) fail(`Missing ${baseChainName} in deployment.json`);

const config = {
  network: deployment.network || env.WORMHOLE_NETWORK || 'Testnet',
  tokenSymbol: env.BASE_TOKEN_SYMBOL || env.VITE_GOLD_SYMBOL || 'GOLD',
  iconUrl: env.VITE_GOLD_ICON_URL || '/gold-token.svg',
  solana: {
    chain: solanaChainName,
    token: solana.token || env.SOLANA_TOKEN_MINT || '',
    manager: solana.manager || env.SOLANA_NTT_MANAGER_ADDRESS || '',
    transceiver: transceiverAddress(solana) || env.SOLANA_NTT_TRANSCEIVER_ADDRESS || '',
    decimals: Number(env.SOLANA_TOKEN_DECIMALS || 6),
    rpcUrl: env.SOLANA_RPC_URL || '',
    explorerCluster: env.VITE_SOLANA_EXPLORER_CLUSTER || (deployment.network === 'Mainnet' ? '' : 'devnet')
  },
  base: {
    chain: baseChainName,
    token: base.token || env.BASE_TOKEN_ADDRESS || '',
    manager: base.manager || env.BASE_NTT_MANAGER_ADDRESS || '',
    transceiver: transceiverAddress(base) || env.BASE_NTT_TRANSCEIVER_ADDRESS || '',
    decimals: Number(env.SOLANA_TOKEN_DECIMALS || 6),
    rpcUrl: env.BASE_RPC_URL || '',
    explorerUrl: env.BASE_EXPLORER_URL || (baseChainName === 'Base' ? 'https://basescan.org' : 'https://sepolia.basescan.org')
  },
  walletConnectProjectId: env.VITE_WALLET_CONNECT_PROJECT_ID || ''
};

const outDir = path.join(root, 'apps/web/src/generated');
fs.mkdirSync(outDir, { recursive: true });
const outFile = path.join(outDir, 'goldDeployment.ts');
fs.writeFileSync(outFile, `// Generated by scripts/09-export-web-config.mjs.\nimport type { GoldDeployment } from '../types';\n\nexport const goldDeployment: GoldDeployment = ${JSON.stringify(config, null, 2)};\n`, 'utf8');
console.log(`Wrote ${path.relative(root, outFile)}`);
